#!/usr/bin/env node
// Reads projects.json and generates src/projects/index.ts.
// Run automatically before `remotion preview` via the preview npm script.

const fs   = require('fs');
const path = require('path');

const root         = path.resolve(__dirname, '..');
const manifestPath = path.join(root, 'projects.json');
const outPath      = path.join(root, 'src', 'projects', 'index.ts');

if (!fs.existsSync(manifestPath)) {
  console.error('projects.json not found');
  process.exit(1);
}

const { projects } = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));

if (!Array.isArray(projects) || projects.length === 0) {
  console.error('projects.json must have a non-empty "projects" array');
  process.exit(1);
}

const imports = projects.map((name, i) =>
  `import { compositions as _p${i} } from './${name}';`
).join('\n');

const exports = `export const compositions = [${projects.map((_, i) => `..._p${i}`).join(', ')}];`;

const out = `// AUTO-GENERATED by scripts/sync-projects.js — do not edit by hand.
${imports}

${exports}
`;

fs.writeFileSync(outPath, out, 'utf8');
console.log(`synced ${projects.length} project(s) → src/projects/index.ts`);
